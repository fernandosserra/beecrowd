/* Including libraries*/
#include <stdio.h>

/*Main function*/
int main(){
    /*Variable declaration and initialization*/
    int a, b, prod;

    /*Requesting values to user*/
    scanf("%d", &a);
    scanf("%d", &b);
    
    /*Doing the calculations*/
    prod = a * b;

    /*Showing results*/
    printf("PROD = %d\n", prod);
    
}

