/* Including libraries*/
#include <stdio.h>

/*Main function*/
int main() {
    /*Variable declaration and initialization*/
    int a, b, soma;
    
    /*Requesting values to user*/
    scanf("%d", &a);
    scanf("%d", &b);
    
    /*Doing the calculations*/
    soma = a + b;

    /*Showing results*/
    printf("SOMA = %d\n", soma);
    
    return 0;
}

